var ObjectRenderingParameters = function(x, y, angle, width, height, alpha, graphicsType, fillStyle, zIndex)
{
	this.x = x;
	this.y = y;
	this.angle = angle;
	this.width = width;
	this.height = height;
	this.graphicsType = graphicsType;
	this.alpha = alpha;
	this.fillStyle = fillStyle;
	this.zIndex = 0;
};
